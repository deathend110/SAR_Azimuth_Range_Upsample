function V4_ValidateResults()
% 自动验收V4结果完整性，并与旧Exp1/Exp2做数值回归。

cfg = V4Core.config();
main_dir = fullfile(cfg.output_root, "MainEvaluation");
mechanism_dir = fullfile(cfg.output_root, "Mechanism");
rsft_calibration_dir = cfg.rsft_calibration_dir;
rsft_evaluation_dir = cfg.rsft_evaluation_dir;

new_summary_path = fullfile(main_dir, "V4_Main_Summary.csv");
new_detail_path = fullfile(main_dir, "V4_Main_Detail.csv");
table_iii_path = fullfile(main_dir, "V4_TableIII_Metrics.csv");
old_summary_path = fullfile( ...
    cfg.repo_root, "Exp1_MainResult_Output", "Exp1_MainResult_Summary.csv");

required_files = [ ...
    string(new_summary_path), ...
    string(new_detail_path), ...
    string(table_iii_path), ...
    string(fullfile(main_dir, "V4_ENL_ROI_Manifest.csv")), ...
    string(fullfile(main_dir, "V4_ENL_ROI_Audit.png")), ...
    string(fullfile(mechanism_dir, "V4_Mechanism_Metrics.csv")), ...
    string(fullfile(mechanism_dir, "V4_Scene_Metrics.csv")), ...
    string(fullfile(mechanism_dir, "V4_Mechanism_Data.mat")), ...
    string(fullfile(mechanism_dir, "V4_Mechanism_Common4x4.png")), ...
    string(fullfile(mechanism_dir, "V4_Scene_SharedColorbar.png")), ...
    string(fullfile(rsft_calibration_dir, ...
        "V4_RSFT_Calibration_Grid.csv")), ...
    string(fullfile(rsft_calibration_dir, ...
        "V4_RSFT_Calibration_Summary.csv")), ...
    string(fullfile(rsft_evaluation_dir, ...
        "V4_RSFT_Main_Summary.csv")), ...
    string(fullfile(rsft_evaluation_dir, ...
        "V4_RSFT_Main_Detail.csv")), ...
    string(fullfile(rsft_evaluation_dir, ...
        "V4_RSFT_TableIII_Metrics.csv")), ...
    string(fullfile(rsft_evaluation_dir, ...
        "V4_RSFT_PairedTests.csv")), ...
    string(fullfile(rsft_evaluation_dir, ...
        "V4_RT_PairedTests.csv")), ...
    string(cfg.rt_figure), ...
    string(cfg.rsft_figure) ...
];
assert(all(isfile(required_files)), "V4验收所需文件不完整。");

new_summary = readtable(new_summary_path, "TextType", "string");
new_detail = readtable(new_detail_path, "TextType", "string");
table_iii = readtable(table_iii_path, "TextType", "string");
old_summary = readtable(old_summary_path, "TextType", "string");

assert(height(new_summary) == 19, "主结果应包含19个组。");
assert(height(new_detail) == 19 * 70, "逐样本明细行数不正确。");
assert(all(new_summary.SampleCount == 70), "主指标样本数不是70。");
assert(all(new_summary.ENLSampleCount == 20), "ENL样本数不是20。");
assert(all(isfinite(new_summary.PSNR_Mean)), "PSNR存在非有限值。");
assert(all(isfinite(new_summary.SSIM_Mean)), "SSIM存在非有限值。");
assert(all(isfinite(new_summary.Entropy_Mean)), "Entropy存在非有限值。");
assert(all(isfinite(new_summary.ENL_Mean)), "ENL存在非有限值。");
assert(height(table_iii) == 13, "Table III应包含GT、基线和11个分配。");

scene_metrics = readtable(fullfile( ...
    mechanism_dir, "V4_Scene_Metrics.csv"), "TextType", "string");
scene_data = load(fullfile( ...
    mechanism_dir, "V4_Mechanism_Data.mat"), ...
    "scene_rois", "scene_metric_table");
expected_scene_names = ["GT"; "R4A1"; "R1A4"; "R2A2"];
assert(isequal(scene_metrics.Allocation, expected_scene_names), ...
    "Fig. 3场景顺序不正确。");
assert(isinf(scene_metrics.PSNR(1)) && scene_metrics.SSIM(1) == 1, ...
    "Fig. 3的GT自比较指标不正确。");
assert(all(isfinite(scene_metrics.PSNR(2:end))) && ...
    all(isfinite(scene_metrics.SSIM(2:end))), ...
    "Fig. 3重建图存在非有限指标。");
assert(isequal(scene_metrics.Allocation, ...
    scene_data.scene_metric_table.Allocation) && ...
    isequal(isinf(scene_metrics.PSNR), ...
    isinf(scene_data.scene_metric_table.PSNR)) && ...
    max(abs(scene_metrics.PSNR(2:end) - ...
    scene_data.scene_metric_table.PSNR(2:end))) < 1e-12 && ...
    max(abs(scene_metrics.SSIM - ...
    scene_data.scene_metric_table.SSIM)) < 1e-12, ...
    "Fig. 3的CSV与MAT指标不一致。");
for scene_idx = 1:height(scene_metrics)
    scene = scene_data.scene_rois(:, :, scene_idx);
    img_gt = scene_data.scene_rois(:, :, 1);
    reproduced_psnr = psnr(scene, img_gt);
    reproduced_ssim = ssim(scene, img_gt);
    if isinf(reproduced_psnr)
        psnr_matches = isinf(scene_metrics.PSNR(scene_idx));
    else
        psnr_matches = abs(scene_metrics.PSNR(scene_idx) - ...
            reproduced_psnr) < 1e-12;
    end
    assert(psnr_matches && ...
        abs(scene_metrics.SSIM(scene_idx) - reproduced_ssim) < 1e-12, ...
        "Fig. 3指标无法由图中归一化幅度重现。");
end

rsft_grid = readtable(fullfile(rsft_calibration_dir, ...
    "V4_RSFT_Calibration_Grid.csv"), "TextType", "string");
rsft_parameters = readtable(fullfile(rsft_calibration_dir, ...
    "V4_RSFT_Calibration_Summary.csv"), "TextType", "string");
rsft_summary = readtable(fullfile(rsft_evaluation_dir, ...
    "V4_RSFT_Main_Summary.csv"), "TextType", "string");
rsft_detail = readtable(fullfile(rsft_evaluation_dir, ...
    "V4_RSFT_Main_Detail.csv"), "TextType", "string");
rsft_table_iii = readtable(fullfile(rsft_evaluation_dir, ...
    "V4_RSFT_TableIII_Metrics.csv"), "TextType", "string");
rsft_tests = readtable(fullfile(rsft_evaluation_dir, ...
    "V4_RSFT_PairedTests.csv"), "TextType", "string");

required_R = [1, 2, 3, 4, 5, 6, 8, 9, 10].';
assert(isequal(rsft_parameters.Range_q, required_R), ...
    "RSFT参数表的距离倍率不完整或顺序错误。");
assert(all(rsft_parameters.SampleCount == 70), ...
    "RSFT校准样本数不是70。");
assert(all(isfinite(rsft_grid.PSNR_Mean)) && ...
    all(isfinite(rsft_grid.SSIM_Mean)), ...
    "RSFT参数网格存在非有限指标。");
assert(height(rsft_summary) == 19, "RSFT主结果应包含19个组。");
assert(height(rsft_detail) == 19 * 70, ...
    "RSFT逐样本明细行数不正确。");
assert(height(rsft_table_iii) == 13, ...
    "RSFT Table III(b)应包含13行。");
assert(height(rsft_tests) == numel(cfg.Q_list) && ...
    isequal(rsft_tests.Q.', cfg.Q_list), ...
    "RSFT配对检验预算不完整。");
assert(all(rsft_summary.SampleCount == 70), ...
    "RSFT主结果样本数不是70。");
assert(all(rsft_summary.ENLSampleCount == 20), ...
    "RSFT主结果ENL样本数不是20。");
assert(all(isfinite(rsft_summary.PSNR_Mean)) && ...
    all(isfinite(rsft_summary.SSIM_Mean)) && ...
    all(isfinite(rsft_summary.Entropy_Mean)) && ...
    all(isfinite(rsft_summary.ENL_Mean)), ...
    "RSFT主结果存在非有限指标。");

for r_idx = 1:height(rsft_parameters)
    rows = rsft_summary.Range_q == rsft_parameters.Range_q(r_idx);
    assert(all(rsft_summary.STRdB(rows) == ...
        rsft_parameters.Best_STR_dB(r_idx)) && ...
        all(rsft_summary.F0OverBr(rows) == ...
        rsft_parameters.Best_F0OverBr(r_idx)), ...
        "同一R的RSFT参数不一致。");
end

rt_roi = readtable(fullfile(main_dir, ...
    "V4_ENL_ROI_Manifest.csv"), "TextType", "string");
rsft_roi = readtable(fullfile(rsft_evaluation_dir, ...
    "V4_RSFT_ENL_ROI_Manifest.csv"), "TextType", "string");
assert(isequal(rt_roi, rsft_roi), "RSFT未复用RT的ENL ROI清单。");
assert(abs(rsft_table_iii.Entropy_Mean(1) - ...
    table_iii.Entropy_Mean(1)) < 1e-12 && ...
    abs(rsft_table_iii.ENL_Mean(1) - ...
    table_iii.ENL_Mean(1)) < 1e-12, ...
    "RT与RSFT表中的GT统计量不一致。");

max_psnr_diff = 0;
max_ssim_diff = 0;
for idx = 1:height(new_summary)
    old_idx = find(old_summary.GroupName == new_summary.GroupName(idx), 1);
    assert(~isempty(old_idx), "旧Exp1中缺少组：%s", new_summary.GroupName(idx));
    max_psnr_diff = max(max_psnr_diff, abs( ...
        new_summary.PSNR_Mean(idx) - old_summary.PSNR_Mean(old_idx)));
    max_ssim_diff = max(max_ssim_diff, abs( ...
        new_summary.SSIM_Mean(idx) - old_summary.SSIM_Mean(old_idx)));
end
assert(max_psnr_diff < 1e-5, "V4 PSNR未通过Exp1回归检查。");
assert(max_ssim_diff < 1e-6, "V4 SSIM未通过Exp1回归检查。");

new_mechanism = readtable( ...
    fullfile(mechanism_dir, "V4_Mechanism_Metrics.csv"), ...
    "TextType", "string");
old_mechanism = readtable(fullfile( ...
    cfg.repo_root, "Exp2_Mechanism_Output", ...
    "Exp2_Mechanism_Metrics.csv"), "TextType", "string");
old_node2 = old_mechanism(old_mechanism.node_name == "node2_rc", :);
old_node2.case_name(old_node2.case_name == "R1A1_NoUp") = "R1A1";

for idx = 1:height(new_mechanism)
    old_idx = find(old_node2.case_name == new_mechanism.Allocation(idx), 1);
    assert(~isempty(old_idx), ...
        "旧Exp2中缺少机制组：%s", new_mechanism.Allocation(idx));
    assert(abs(new_mechanism.OffSupport(idx) - ...
        old_node2.off_support_ratio(old_idx)) < 1e-6);
    assert(abs(new_mechanism.RangeLeakage(idx) - ...
        old_node2.range_leakage_ratio(old_idx)) < 1e-6);
    assert(abs(new_mechanism.AzimuthLeakage(idx) - ...
        old_node2.azimuth_leakage_ratio(old_idx)) < 1e-6);
end

fprintf("V4结果验收通过。\n");
fprintf("  主结果组数：%d\n", height(new_summary));
fprintf("  逐样本行数：%d\n", height(new_detail));
fprintf("  Max |dPSNR|：%.3g dB\n", max_psnr_diff);
fprintf("  Max |dSSIM|：%.3g\n", max_ssim_diff);
fprintf("  ENL样本数：%d\n", new_summary.ENLSampleCount(1));
fprintf("  Fig. 3场景指标数：%d\n", height(scene_metrics));
fprintf("  RSFT校准距离倍率：%d\n", height(rsft_parameters));
fprintf("  RSFT主结果组数：%d\n", height(rsft_summary));
end
