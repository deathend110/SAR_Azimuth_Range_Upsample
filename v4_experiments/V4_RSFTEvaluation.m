function V4_RSFTEvaluation()
% 使用按距离倍率锁定的RSFT参数运行完整固定预算分配实验。

cfg = V4Core.config();
addpath(cfg.repo_root, cfg.experiment_dir);
V4Core.ensureDir(cfg.rsft_evaluation_dir);

calibration_path = fullfile(cfg.rsft_calibration_dir, ...
    "V4_RSFT_Calibration_Summary.csv");
rt_data_path = fullfile( ...
    cfg.output_root, "MainEvaluation", "V4_Main_Data.mat");
assert(isfile(calibration_path), "缺少统一RSFT校准结果。");
assert(isfile(rt_data_path), "缺少现有RT主实验数据。");

parameter_table = readtable(calibration_path, "TextType", "string");
S60 = load(cfg.parameter_file);
[sample_cache, sample_manifest] = V4Core.buildSampleCache(cfg, S60);
total_samples = numel(sample_cache);
group_defs = V4Core.buildGroupDefinitions(cfg.Q_list);
num_groups = numel(group_defs);
initial_phase = 0;

rt_data = load(rt_data_path, ...
    "sample_manifest", "roi_manifest", "gt_entropy", "gt_enl", ...
    "summary_table", "psnr_all", "ssim_all");
assert(isequal(sample_manifest, rt_data.sample_manifest), ...
    "RSFT样本清单与现有RT主实验不一致。");
[is_enl_sample, enl_top, enl_left] = mapROIs( ...
    sample_manifest, rt_data.roi_manifest);

required_R = unique([group_defs.Range_q]).';
assert(isequal(sort(parameter_table.Range_q), sort(required_R)), ...
    "RSFT参数表未完整覆盖主实验所需距离倍率。");
[group_STR_dB, group_f0_over_Br] = ...
    assignGroupParameters(group_defs, parameter_table);

psnr_all = nan(num_groups, total_samples);
ssim_all = nan(num_groups, total_samples);
entropy_all = nan(num_groups, total_samples);
enl_all = nan(num_groups, total_samples);
completed_groups = false(num_groups, 1);
checkpoint_path = fullfile(cfg.rsft_evaluation_dir, ...
    "V4_RSFT_Evaluation_Checkpoint.mat");
signature = struct( ...
    "seed", cfg.seed, ...
    "Q_list", cfg.Q_list, ...
    "total_samples", total_samples, ...
    "Range_q", parameter_table.Range_q.', ...
    "STR_dB", parameter_table.Best_STR_dB.', ...
    "f0_over_Br", parameter_table.Best_F0OverBr.', ...
    "initial_phase", initial_phase);

if isfile(checkpoint_path)
    checkpoint = load(checkpoint_path);
    assert(isfield(checkpoint, "signature") && ...
        isequaln(checkpoint.signature, signature), ...
        "现有RSFT评价检查点与当前配置不匹配，请人工核查。");
    psnr_all = checkpoint.psnr_all;
    ssim_all = checkpoint.ssim_all;
    entropy_all = checkpoint.entropy_all;
    enl_all = checkpoint.enl_all;
    completed_groups = checkpoint.completed_groups;
    fprintf("恢复RSFT评价检查点：已完成%d/%d组。\n", ...
        sum(completed_groups), num_groups);
end

fprintf("\n=== V4 RSFT完整评价：%d组 x %d样本 ===\n", ...
    num_groups, total_samples);
for group_idx = 1:num_groups
    current = group_defs(group_idx);
    if completed_groups(group_idx)
        fprintf("[%02d/%02d] %s：已完成，跳过。\n", ...
            group_idx, num_groups, current.GroupName);
        continue;
    end

    fprintf("[%02d/%02d] %s，STR=%+.1f dB，f0/Br=%.1f\n", ...
        group_idx, num_groups, current.GroupName, ...
        group_STR_dB(group_idx), group_f0_over_Br(group_idx));
    for sample_idx = 1:total_samples
        img_gt = sample_cache(sample_idx).img_gt;
        img_out = V4Core.buildRSFTImage( ...
            sample_cache(sample_idx).signal60_input, S60, ...
            current.Range_q, current.Azimuth_q, ...
            group_STR_dB(group_idx), ...
            group_f0_over_Br(group_idx), initial_phase);
        psnr_all(group_idx, sample_idx) = psnr(img_out, img_gt);
        ssim_all(group_idx, sample_idx) = ssim(img_out, img_gt);
        entropy_all(group_idx, sample_idx) = ...
            V4Core.imageEntropy(img_out, cfg.entropy_num_bins);
        if is_enl_sample(sample_idx)
            enl_all(group_idx, sample_idx) = V4Core.enl( ...
                img_out, enl_top(sample_idx), enl_left(sample_idx), ...
                cfg.enl_window_size);
        end
    end

    completed_groups(group_idx) = true;
    save(checkpoint_path, ...
        "signature", "psnr_all", "ssim_all", ...
        "entropy_all", "enl_all", "completed_groups", "-v7.3");
end

summary_table = buildSummaryTable( ...
    group_defs, group_STR_dB, group_f0_over_Br, S60.B, ...
    psnr_all, ssim_all, entropy_all, enl_all);
detail_table = buildDetailTable( ...
    group_defs, group_STR_dB, group_f0_over_Br, ...
    sample_manifest, psnr_all, ssim_all, entropy_all, enl_all);
gt_summary = buildGTSummary( ...
    total_samples, sum(is_enl_sample), ...
    rt_data.gt_entropy, rt_data.gt_enl);
table_iii = [gt_summary; summary_table(1, :)];
for Q = cfg.table_Q_list
    table_iii = [table_iii; ...
        summary_table(summary_table.Q == Q, :)]; %#ok<AGROW>
end
paired_tests = buildPairedTests( ...
    cfg.Q_list, group_defs, summary_table, psnr_all, ssim_all);
rt_paired_tests = buildPairedTests( ...
    cfg.Q_list, group_defs, rt_data.summary_table, ...
    rt_data.psnr_all, rt_data.ssim_all);

writetable(summary_table, fullfile(cfg.rsft_evaluation_dir, ...
    "V4_RSFT_Main_Summary.csv"));
writetable(detail_table, fullfile(cfg.rsft_evaluation_dir, ...
    "V4_RSFT_Main_Detail.csv"));
writetable(table_iii, fullfile(cfg.rsft_evaluation_dir, ...
    "V4_RSFT_TableIII_Metrics.csv"));
writetable(paired_tests, fullfile(cfg.rsft_evaluation_dir, ...
    "V4_RSFT_PairedTests.csv"));
writetable(rt_paired_tests, fullfile(cfg.rsft_evaluation_dir, ...
    "V4_RT_PairedTests.csv"));
copyfile(fullfile(cfg.output_root, "MainEvaluation", ...
    "V4_ENL_ROI_Manifest.csv"), fullfile(cfg.rsft_evaluation_dir, ...
    "V4_RSFT_ENL_ROI_Manifest.csv"));

save(fullfile(cfg.rsft_evaluation_dir, ...
    "V4_RSFT_Main_Data.mat"), ...
    "cfg", "signature", "group_defs", "sample_manifest", ...
    "parameter_table", "group_STR_dB", "group_f0_over_Br", ...
    "psnr_all", "ssim_all", "entropy_all", "enl_all", ...
    "summary_table", "detail_table", "table_iii", ...
    "paired_tests", "rt_paired_tests", "-v7.3");
writeMetadata(cfg, signature, paired_tests, rt_paired_tests);
fprintf("RSFT完整评价完成：%s\n", cfg.rsft_evaluation_dir);
end

function [is_enl_sample, enl_top, enl_left] = ...
        mapROIs(sample_manifest, roi_manifest)
total_samples = height(sample_manifest);
is_enl_sample = false(total_samples, 1);
enl_top = zeros(total_samples, 1);
enl_left = zeros(total_samples, 1);
for idx = 1:height(roi_manifest)
    sample_idx = find( ...
        sample_manifest.SampleID == roi_manifest.SampleID(idx), 1);
    assert(~isempty(sample_idx), "ENL ROI样本不在统一样本清单中。");
    is_enl_sample(sample_idx) = true;
    enl_top(sample_idx) = roi_manifest.ROITop(idx);
    enl_left(sample_idx) = roi_manifest.ROILeft(idx);
end
assert(sum(is_enl_sample) == 20, "ENL ROI数量不是20。");
end

function [STR_dB, f0_over_Br] = ...
        assignGroupParameters(group_defs, parameter_table)
num_groups = numel(group_defs);
STR_dB = nan(num_groups, 1);
f0_over_Br = nan(num_groups, 1);
for group_idx = 1:num_groups
    row = parameter_table( ...
        parameter_table.Range_q == group_defs(group_idx).Range_q, :);
    assert(height(row) == 1, "每个R必须且只能对应一组RSFT参数。");
    STR_dB(group_idx) = row.Best_STR_dB;
    f0_over_Br(group_idx) = row.Best_F0OverBr;
end
end

function summary_table = buildSummaryTable( ...
        group_defs, STR_dB, f0_over_Br, bandwidth_Hz, ...
        psnr_all, ssim_all, entropy_all, enl_all)
num_groups = numel(group_defs);
Q = [group_defs.Q].';
GroupName = string({group_defs.GroupName}).';
Range_q = [group_defs.Range_q].';
Azimuth_q = [group_defs.Azimuth_q].';
GroupType = string({group_defs.GroupType}).';
SampleCount = repmat(size(psnr_all, 2), num_groups, 1);
ENLSampleCount = sum(~isnan(enl_all), 2);
F0Hz = f0_over_Br * bandwidth_Hz;

summary_table = table( ...
    Q, GroupName, Range_q, Azimuth_q, GroupType, ...
    SampleCount, ENLSampleCount, STR_dB, f0_over_Br, F0Hz, ...
    mean(psnr_all, 2, "omitnan"), std(psnr_all, 0, 2, "omitnan"), ...
    mean(ssim_all, 2, "omitnan"), std(ssim_all, 0, 2, "omitnan"), ...
    mean(entropy_all, 2, "omitnan"), std(entropy_all, 0, 2, "omitnan"), ...
    mean(enl_all, 2, "omitnan"), std(enl_all, 0, 2, "omitnan"), ...
    'VariableNames', { ...
    'Q', 'GroupName', 'Range_q', 'Azimuth_q', 'GroupType', ...
    'SampleCount', 'ENLSampleCount', 'STRdB', 'F0OverBr', 'F0Hz', ...
    'PSNR_Mean', 'PSNR_Std', 'SSIM_Mean', 'SSIM_Std', ...
    'Entropy_Mean', 'Entropy_Std', 'ENL_Mean', 'ENL_Std'});
end

function detail_table = buildDetailTable( ...
        group_defs, group_STR_dB, group_f0_over_Br, ...
        sample_manifest, psnr_all, ssim_all, entropy_all, enl_all)
num_groups = numel(group_defs);
total_samples = height(sample_manifest);
num_rows = num_groups * total_samples;

Q = zeros(num_rows, 1);
GroupName = strings(num_rows, 1);
Range_q = zeros(num_rows, 1);
Azimuth_q = zeros(num_rows, 1);
GroupType = strings(num_rows, 1);
STRdB = zeros(num_rows, 1);
F0OverBr = zeros(num_rows, 1);
SampleID = zeros(num_rows, 1);
Dataset = strings(num_rows, 1);
File = strings(num_rows, 1);
CStart = zeros(num_rows, 1);
PSNR = zeros(num_rows, 1);
SSIM = zeros(num_rows, 1);
Entropy = zeros(num_rows, 1);
ENL = nan(num_rows, 1);

ptr = 0;
for group_idx = 1:num_groups
    rows = ptr + (1:total_samples);
    current = group_defs(group_idx);
    Q(rows) = current.Q;
    GroupName(rows) = current.GroupName;
    Range_q(rows) = current.Range_q;
    Azimuth_q(rows) = current.Azimuth_q;
    GroupType(rows) = current.GroupType;
    STRdB(rows) = group_STR_dB(group_idx);
    F0OverBr(rows) = group_f0_over_Br(group_idx);
    SampleID(rows) = sample_manifest.SampleID;
    Dataset(rows) = sample_manifest.Dataset;
    File(rows) = sample_manifest.File;
    CStart(rows) = sample_manifest.CStart;
    PSNR(rows) = psnr_all(group_idx, :).';
    SSIM(rows) = ssim_all(group_idx, :).';
    Entropy(rows) = entropy_all(group_idx, :).';
    ENL(rows) = enl_all(group_idx, :).';
    ptr = ptr + total_samples;
end

detail_table = table( ...
    Q, GroupName, Range_q, Azimuth_q, GroupType, ...
    STRdB, F0OverBr, SampleID, Dataset, File, CStart, ...
    PSNR, SSIM, Entropy, ENL);
end

function gt_summary = buildGTSummary( ...
        total_samples, enl_sample_count, gt_entropy, gt_enl)
gt_summary = table( ...
    0, "GT", 0, 0, "reference", total_samples, enl_sample_count, ...
    NaN, NaN, NaN, Inf, NaN, 1, 0, ...
    mean(gt_entropy, "omitnan"), std(gt_entropy, 0, "omitnan"), ...
    mean(gt_enl, "omitnan"), std(gt_enl, 0, "omitnan"), ...
    'VariableNames', { ...
    'Q', 'GroupName', 'Range_q', 'Azimuth_q', 'GroupType', ...
    'SampleCount', 'ENLSampleCount', 'STRdB', 'F0OverBr', 'F0Hz', ...
    'PSNR_Mean', 'PSNR_Std', 'SSIM_Mean', 'SSIM_Std', ...
    'Entropy_Mean', 'Entropy_Std', 'ENL_Mean', 'ENL_Std'});
end

function paired_tests = buildPairedTests( ...
        Q_list, group_defs, summary_table, psnr_all, ssim_all)
num_Q = numel(Q_list);
Q = Q_list(:);
BestBidirectional = strings(num_Q, 1);
BestUnidirectional = strings(num_Q, 1);
DeltaPSNR = zeros(num_Q, 1);
DeltaSSIM = zeros(num_Q, 1);
PValuePSNR = zeros(num_Q, 1);
PValueSSIM = zeros(num_Q, 1);

group_types = string({group_defs.GroupType}).';
for q_idx = 1:num_Q
    current_Q = Q(q_idx);
    bidir_idx = find( ...
        [group_defs.Q].' == current_Q & ...
        ismember(group_types, ["balanced", "mixed"]));
    unidir_idx = find( ...
        [group_defs.Q].' == current_Q & ...
        ismember(group_types, ["range_only", "azimuth_only"]));
    assert(~isempty(bidir_idx) && ~isempty(unidir_idx), ...
        "预算Q=%d缺少双向或单向组。", current_Q);

    [~, local_b] = max(summary_table.PSNR_Mean(bidir_idx));
    [~, local_u] = max(summary_table.PSNR_Mean(unidir_idx));
    best_b = bidir_idx(local_b);
    best_u = unidir_idx(local_u);
    BestBidirectional(q_idx) = summary_table.GroupName(best_b);
    BestUnidirectional(q_idx) = summary_table.GroupName(best_u);
    DeltaPSNR(q_idx) = ...
        summary_table.PSNR_Mean(best_b) - summary_table.PSNR_Mean(best_u);
    DeltaSSIM(q_idx) = ...
        summary_table.SSIM_Mean(best_b) - summary_table.SSIM_Mean(best_u);
    PValuePSNR(q_idx) = signrank( ...
        psnr_all(best_b, :), psnr_all(best_u, :), "tail", "both");
    PValueSSIM(q_idx) = signrank( ...
        ssim_all(best_b, :), ssim_all(best_u, :), "tail", "both");
end

paired_tests = table( ...
    Q, BestBidirectional, BestUnidirectional, ...
    DeltaPSNR, DeltaSSIM, PValuePSNR, PValueSSIM);
end

function writeMetadata(cfg, signature, paired_tests, rt_paired_tests)
metadata_path = fullfile( ...
    cfg.rsft_evaluation_dir, "V4_RSFT_Evaluation_Metadata.txt");
fid = fopen(metadata_path, "w");
cleanup = onCleanup(@() fclose(fid)); %#ok<NASGU>
fprintf(fid, "Experiment=V4_RSFTEvaluation\n");
fprintf(fid, "Seed=%d\n", signature.seed);
fprintf(fid, "SampleCount=%d\n", signature.total_samples);
fprintf(fid, "QList=%s\n", mat2str(signature.Q_list));
fprintf(fid, "RangeQ=%s\n", mat2str(signature.Range_q));
fprintf(fid, "STRdB=%s\n", mat2str(signature.STR_dB));
fprintf(fid, "F0OverBr=%s\n", mat2str(signature.f0_over_Br));
fprintf(fid, "InitialPhase=%.16g\n", signature.initial_phase);
fprintf(fid, "ParameterSelection=R-dependent mean-SSIM best tested settings\n");
fprintf(fid, "ENLROI=Reused from MainEvaluation/V4_ENL_ROI_Manifest.csv\n");
fprintf(fid, "PairedTests=%s\n", jsonencode(table2struct(paired_tests)));
fprintf(fid, "RTPairedTests=%s\n", ...
    jsonencode(table2struct(rt_paired_tests)));
end
