%% ==================== 局部函数 ====================
function value = local_percentile(x, p)
    x = sort(x(isfinite(x)));

    if isempty(x)
        value = NaN;
        return;
    end

    position = 1 + (numel(x) - 1) * p / 100;
    lower_idx = floor(position);
    upper_idx = ceil(position);

    if lower_idx == upper_idx
        value = x(lower_idx);
    else
        value = x(lower_idx) + ...
            (x(upper_idx) - x(lower_idx)) * ...
            (position - lower_idx);
    end
end
